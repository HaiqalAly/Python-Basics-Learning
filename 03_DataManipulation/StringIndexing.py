# Indexing = Accessing elements of a string using their position [] (index).
# [Start:End:Step] (Start is inclusive, End is exclusive)

information = "Hello, welcome to the world of Python programming!"

# Accessing individual characters
print(information[0])      # H
print(information[0:5])    # Hello
print(information[::2])    # Hlo elcm o h olfPto rgamn!
print(information[::-1])   # !gnimmargorp nohtyP fo dlrow eht ot emoclew ,olleH